package Franks_Madlibs.Franks_Madlibs;

public class Prompt {

	private String message;
	private String response;
	private int startOfReplacement;
	private int endOfReplacement;
	
	
	public String getMessage() {
		return message;
	}
	public void setMessage(String message) {
		this.message = message;
	}
	public String getResponse() {
		return response;
	}
	public void setResponse(String response) {
		this.response = response;
	}
	public int getStartOfReplacement() {
		return startOfReplacement;
	}
	public void setStartOfReplacement(int startOfReplacement) {
		this.startOfReplacement = startOfReplacement;
	}
	public int getEndOfreplacement() {
		return endOfReplacement;
	}
	public void setEndOfreplacement(int endOfreplacement) {
		this.endOfReplacement = endOfreplacement;
	}
	
	
	
	
}
